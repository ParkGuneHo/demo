package com.example.demo;

public class CharacterModel {
    private String name; private int level; private int life; public CharacterModel(String name, int level, int life) { this.name = name; this.level = level; this.life = life; } public String getName() { return name; } public void setName(String name) { this.name = name; } public int getLevel() { return level; } public void setLevel(int level) { this.level = level; } public int getLife() { return life; } public void setLife(int life) { this.life = life; }

}
